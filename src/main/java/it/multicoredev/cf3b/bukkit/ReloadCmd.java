public class ReloadCmd implements CommandExecutor {
    private final Plugin plugin;

    public ReloadCmd(Plugin plugin) {
        this.plugin = plugin;
    }

    @Override
    public boolean onCommand(@NotNull CommandSender sender, @NotNull Command command, @NotNull String label, @NotNull String[] args) {
        if (!sender.hasPermission("f3b.reload")) {
            Text.get().send("<red>You don't have permission to use this command!</red>", sender);
            return true;
        }

        plugin.onDisable();
        plugin.onEnable();
        Text.get().send("<dark_green>CustomF3Brand reloaded!</dark_green>", sender);
        return true;
    }
}
